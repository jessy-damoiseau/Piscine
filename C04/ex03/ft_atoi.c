#include <unistd.h>
#include <stdio.h>

int		ft_atoi(char *str)
{
	int i;
	int sign;
	int count;

	i = 0;
	sign = 1;
	count = 0;
	while (str[i] && (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
				|| str[i] == '\r' || str[i] == '\f' || str[i] == ' '))
		i++;
	while (str[i] && (str[i] == '-' || str[i] == '+'))
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		count = count * 10 + str[i] - '0';
		i++;
	}
	return (count * sign);
}
