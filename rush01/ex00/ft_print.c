#include <unistd.h>

void	ft_print(int *map)
{
	int		i;
	char	c;

	i = 0;
	while (i < 16)
	{
		c = map[i] + 48;
		write(1, &c, 1);
		write(1, " ", 1);
		if (i % 4 == 3)
			write(1, "\n", 1);
		i++;
	}
}
