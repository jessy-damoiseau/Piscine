#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*tab;

	i = 0;
	while (src[i])
		i++;
	tab = malloc(sizeof(*tab) * i);
	i = 0;
	while (src[i])
	{
		tab[i] = src[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
