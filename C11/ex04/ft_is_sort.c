int		ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int i;
	int j;

	i = 1;
	while (i <= length)
	{
		j = f(tab[i - 1], tab[i]);
		i++;
		if (f(tab[i - 1], tab[i]) != j)
			return (0);
	}
	return (1);
}
